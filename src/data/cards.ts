// Static card and benefit data (no database required)

export interface Card {
  id: string
  issuer: string
  productName: string
  network: string
  annualFee: number
  active: boolean
  benefits: Benefit[]
  welcomeBonus?: WelcomeBonus
}

export type BenefitType =
  | 'RECURRING_CREDIT'      // Credits that reset (Uber, Dining)
  | 'MULTIPLIER'            // Points/cash back multipliers
  | 'INSURANCE'             // Passive protections (trip insurance, purchase protection)
  | 'SUBSCRIPTION'          // Service subscriptions (Priority Pass, DashPass)
  | 'STATUS'                // Elite status benefits (IHG Platinum)
  | 'THRESHOLD_CREDIT'      // Spend-to-unlock benefits (IHG Diamond at $75k)
  | 'PER_BOOKING'           // Per-transaction benefits ($100 hotel credit)
  | 'ROADSIDE'              // Countable services (4 roadside assists/year)
  | 'INFORMATIONAL'         // Static benefits (no foreign transaction fees)

export type CycleType =
  | 'MONTHLY'
  | 'CALENDAR_YEAR'
  | 'CARDMEMBER_YEAR'
  | 'SEMIANNUAL_CALENDAR'
  | 'ONE_TIME'
  | 'ONGOING'               // Never expires (multipliers, insurance)
  | 'SPENDING_THRESHOLD'    // Unlocks at spending threshold

export type TrackingType =
  | 'SPENDING'              // Track dollar amount spent
  | 'COUNTER'               // Track number of events
  | 'BOOLEAN'               // Simple yes/no (activated/enrolled)
  | 'NONE'                  // No tracking needed (passive benefits)

export interface Benefit {
  id: string
  name: string
  type: BenefitType
  nominalValue: number | null
  currency: string
  program?: string | null
  cycleType: CycleType
  cycleDefinition: string // JSON string
  usageLimitPerCycle: number | null
  triggerDescription: string
  exclusionsSummary: string
  priorityScore: number
  officialUrl: string
  active: boolean

  // New tracking fields
  trackingType: TrackingType
  requiresEnrollment?: boolean
  enrollmentUrl?: string | null
  expirationDate?: string | null        // For time-limited offers
  spendingThreshold?: number | null     // Amount to spend to unlock benefit
  eventLimit?: number | null            // For countable benefits (roadside: 4/year)

  // UI display hints
  category?: 'TRAVEL' | 'DINING' | 'EARNING' | 'PROTECTION' | 'LIFESTYLE' | 'OTHER'
  icon?: string                         // Emoji or icon name for UI
}

export interface WelcomeBonus {
  id: string
  requiredSpend: number
  spendWindowStart: string
  spendWindowEnd: string
  earned: boolean
  earnedDate?: string | null
  expectedPoints: number
  actualPoints?: number | null
  program: string
}

